                    En paralelo, desde el 2012, el Proyecto Emblemático de Reconversión de la Educación Técnica y Tecnológica 
                    Superior Pública del país, busca fortalecer este sistema de educación superior, repotenciando física y 
                    académicamente a su estructura. Por tanto, la edificación, que antes fue una cárcel municipal y que se 
                    incendió en el 2006, fue restaurada y recuperada para ponerse al servicio de la educación superior del 
                    Ecuador con una inversión aproximada a los 9 millones de dólares de parte de Senescyt.
