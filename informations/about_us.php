                    <div class="col-md" style="text-align: justify;" >
                    El antiguo edificio ubicado en las calles Ambato y García Moreno de la ciudad de Quito, 
                    recluyó a cientos de personas privadas de la libertad en el pasado. Durante décadas, este 
                    inmueble tuvo ese uso y se lo conoció como la Cárcel #2. Sin embargo, el lunes 25 de marzo 
                    de 2013, se marcó un giro definitivo en el destino de esta edificación, ya que el Ministerio 
                    Coordinador de Patrimonio, el Municipio del Distrito Metropolitano de Quito y la Secretaría 
                    Nacional de Educación Superior, Ciencia y Tecnología e Innovación (Senescyt), firmaron un convenio 
                    tripartito de cooperación interinstitucional, donde se establece la creación del primer Instituto Tecnológico 
                    Superior, Sectorial, de Turismo y Patrimonio de la nación.
                    </div>
